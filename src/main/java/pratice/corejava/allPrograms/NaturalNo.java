package pratice.corejava.allPrograms;

public class NaturalNo {
    public static void main(String[] args) {
        int sum=0;
        int n=100;
        {
            for (int i=1;i<=n;i++){
                sum=sum+i;
            }
            System.out.println("sum of 1 to 100 is :"+sum);
        }
    }
}
