package pratice.newJava.strings;

public class StringPrograms {
    public static void main(String[] args) {
        String[] str = {"Shri", "niwas", "Alle"};
        char[] ch = {'N', 'I', 'K', 'H', 'I', 'L'};
        for (String st : str) {

            System.out.println(st);
        }
        for (char c : ch) {

            System.out.println(c);
        }
        for (int i=0;i<=8;i++){
            System.out.println(ch);
        }
    }

}
